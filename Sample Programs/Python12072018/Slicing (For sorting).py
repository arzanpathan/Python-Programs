#Slicing
st='Python Language'
print(st[1:5]) # for printing string between 1 to 4


print(st[:5])#for printing from start to 5-1 i.e 0 to 4

print(st[7:])

print(st[:])

print(st[1:4:1])

print(st[1::2])# since 2 interval of 1 i.e it will print after skipping next value

print(st[::3])#  interval of 2

print(st[-1])

print(st[-15:0])

print(st[::-1]) # For printing in reverse

